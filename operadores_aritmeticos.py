'''

+ -> adição
- -> subtração
* -> multiplicação
/ -> divisão
// -> divisão inteira
** -> exponenciação
% -> resto da divisão, módulo


'''

#operações básicas da matemática

print( 25 * '=')
print('Operações básicas')
print( 25 * '=')

print('Soma:', 10 + 2)
print('Subtração:', 10 - 2)
print('Multiplicação:', 10 * 2)
print('Divisão:', 10 / 2)
print('Divisão Inteira:', 10 // 2)
print('Potenciação:', 10 ** 2)
print('Resto da divisão:', 10 % 2)


